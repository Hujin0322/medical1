#동전 교환 프로그램
#money=int(input('교환할 돈을 입력하세요 >> '))
money=int(input('교환할 돈을 입력하세요 >> '))
c500, c100, c50, c10 =0,0,0,0
#500원 동전개수
c500 = int(money//500) #몫
#100원 동전개수
c100 = int((money%500)//100)
#50원 동전개수
c50 = int(((money%500)%100)//50)
#10원 동전개수
c10= int((((money%500)%100)%50)//10)
print('입력한 금액:{}'.format(money))
print('500원:{}\n100원:{}\n50원:{}\n10원:{}'.format(c500,c100,c50,c10))
